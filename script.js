function navigatePage(page) {
  switch (page) {
    case 0:
      window.location.href = "index.html";
      break;
    case 1:
      window.location.href = "educationSystem.html";
      break;
    case 2:
      window.location.href = "writingAndReading.html";
      break;
    default:
      window.location.href = "technology.html";
      break;

  }
}

